                "SGTC: How Fuel Monitoring and Geo Fencing Boosted Logistics Efficiency and Cut Fuel Costs"
                </h3>
                <p>
                Subash Goods Transport Co. (SGTC) is a logistics company in India that provides transport services to a wide range of customers. With a fleet of over 20 vehicles, SGTC faces significant challenges in managing fuel expenses, preventing fuel theft, optimizing fuel usage per route, and tracking vehicles in real-time.
                </p>
