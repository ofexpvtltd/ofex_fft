                <h3>THE CHALLENGE</h3>
                <p><br>
                As expenditure on fuel and maintenance rose, ARSS became concerned that business was slowing down and that construction projects were becoming less profitable. ARSS needed to optimize their cashflow management in a competitive and challenging environment in order to pay salaries and clear vendor payments on time. In addition, vehicle mileage was extremely below the OEM’s indicated mileage figures. ARSS knew that some of these issues were due to fuel pilferage. Fuel was often drained directly from the tank between journeys, which would eventually cause the vehicle to run out of fuel and result in damage to the fuel pump. Replacing fuel pumps was a major expense.
                </p>
                <p>ARSS needed a fuel management solution to reduce operating costs, prevent fuel pilferage incidents, ensure that vehicles were getting maximum mileage and automate the monitoring of fuel levels, which was previously conducted manually by drivers using a yardstick and paper-based data entry system.</p>
                <h3>THE SOLUTION</h3>
                <p><br>
                ARSS decided to install  OFeX LLS-4 fuel level sensors in two Tata tipper trucks as a test project in September 2019. The fuel-level sensors were integrated with third-party GPS trackers and fleet management software by the technical experts of Nigraani.com to provide a complete fuel and fleet management solution.
                <br><b>“ARSS was so impressed by the results of the test run that they immediately installed  OFeX fuel-level sensors in 350 vehicles, and installation on another 200 vehicles is in progress. ARSS plans to equip its entire fleet of 2500+ vehicles with  OFeX sensors. ”</b>- Mr. Sumeet Agarwal, Director & CEO Nigraani.com
                </p>
                <img src="assets/img/casestudy/cs1a.jpg" class="img-fluid" alt="">

                <h3>THE RESULTS<br></h3>
                <p><br>
                ARSS has reported significant results using OFeX fuel-level sensors:<br>
                •	In the test project, vehicle mileage increased from 1.7 KM/liter to 3.2 KM/liter<br>
                •	Fuel pump damage incidents stopped, eliminating major replacement costs<br>
                •	Repairs reduced by 60% as fuel drains are detected instantly, preventing further damage<br>
                •	24% less fuel consumption in October 2019 compared with October 2018<br>
                •	Saving ₹1Cr/month with 350 vehicles equipped with OFeX sensors<br>
                </p>
                
                <p>ARSS is able to monitor, analyse and make rapid decisions about fuel usage using live data, which is a significant time saver. Drivers no longer have to measure and enter fuel data manually, which saves time and avoids errors.</p>
                <p>Drivers and supervisors are more motivated and experience less stress associated with fuel measurement, breakdowns and frequent refuelling. Now ARSS is able to meet construction project deadlines more easily, which improves client relationships. When ARSS analysed vehicle utilization data they were surprised to find that it was possible to reduce the average number of vehicles on site from 10 to 8, getting more work done with less resources.</p>
                <p>Transparent fuel monitoring has increased trust between management and employees, and saving money on fuel has enabled ARSS to increase driver/operator salaries by 200%. The overall atmosphere in the company has improved. Business relationships with vendors are stronger, as more efficient cashflow enables prompt payments, and ARSS now enjoys a shining reputation in the industry.</p>
                <p><b>“OFEX’s solution is not just a fuel monitoring system. It has helped us to maximize our profits, create a happier workforce and increase the brand value of the firm.”</b> – Mr. Rajul Agarwal, Full-Time Director, ARSS Infrastructure Projects Limited.</p>
                

