                <h3>Indian construction company reduces fuel consumption by 24%, cuts repairs by 60% and increases mileage thanks to OFeX solutions.</h3>
                <p>ARSS is one of the fastest-growing construction companies in India, and is currently executing major road and rail infrastructure projects across 14 states. To tackle soaring fuel and vehicle maintenance expenditure, ARSS decided to invest in a fuel management solution. After equipping vehicles with OFeX fuel-level sensors, ARSS experienced a dramatic drop in fuel consumption, repair costs and fuel pump damage incidents.</p>
                <p>With a fleet of 2,500+ vehicles including trucks, tippers, tractors, loaders, cranes, excavators and pavers, ARSS needs to keep a tight rein on fuel and repair costs. Fuel and maintenance expenditure was climbing steeply, affecting ARSS’s profitability and cashflow. ARSS decided to take control of costs by installing  OFeX fuel sensors in vehicles to accurately monitor and manage fuel usage in real time.</p>
                <blockquote>
                  <p>
                  <u>THE NUMBERS</u><br>
                  <b>₹1 Crore plus</b> saved per month <br> <b>24%</b> less fuel consumption in one month <br> <b>60%</b> reduction in repairs
                  </p>
